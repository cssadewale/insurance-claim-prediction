"""---
## 3. Model Evaluation and Selection

Evaluating models on an **imbalanced classification problem** requires more than just accuracy. We use five complementary metrics:

| Metric | Formula | What It Measures |
|--------|---------|-----------------|
| **Accuracy** | (TP + TN) / Total | Overall correct predictions — misleading when imbalanced |
| **Precision** | TP / (TP + FP) | Of all predicted claims, how many were actual claims? |
| **Recall** | TP / (TP + FN) | Of all actual claims, how many did we catch? |
| **F1-Score** | 2 × (P × R) / (P + R) | Harmonic mean of Precision and Recall — balances both |
| **ROC-AUC** | Area under ROC curve | Ability to rank claim vs non-claim across all thresholds |

> **In insurance**: Recall matters most operationally (we want to catch as many claims as possible), but Precision matters commercially (we don't want to penalise low-risk customers unfairly). F1-Score and ROC-AUC balance both concerns.
"""

