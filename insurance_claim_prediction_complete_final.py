"""---
## 4. Hyperparameter Tuning — GridSearchCV on Random Forest

The default Random Forest uses arbitrary hyperparameter settings. We can improve its performance by systematically searching for the **optimal combination of hyperparameters** using `GridSearchCV`.

**Hyperparameters being tuned:**

| Hyperparameter | What it controls |
|---------------|-----------------|
| `n_estimators` | Number of decision trees in the forest |
| `max_features` | Number of features considered at each split |
| `max_depth` | Maximum depth each tree can grow |
| `min_samples_split` | Minimum samples required to split an internal node |
| `min_samples_leaf` | Minimum samples required at a leaf node |

`GridSearchCV` with `cv=5` means every hyperparameter combination is evaluated using **5-fold cross-validation** on the training data — giving a robust, unbiased estimate of each configuration's F1-Score.
"""

from sklearn.model_selection import GridSearchCV
from sklearn.ensemble import RandomForestClassifier

# ── Define the hyperparameter search space ─────────────────────────────────────
param_grid = {
    'n_estimators':      [100, 200, 300],     # number of trees
    'max_features':      ['sqrt', 'log2'],    # features considered per split
    'max_depth':         [10, 20, 30, None],  # None = fully grown trees
    'min_samples_split': [2, 5, 10],          # minimum samples to split a node
    'min_samples_leaf':  [1, 2, 4]            # minimum samples at a leaf
}

# ── Instantiate a fresh RandomForest for tuning ────────────────────────────────
rf_for_tuning = RandomForestClassifier(random_state=42)

# ── Set up GridSearchCV ────────────────────────────────────────────────────────
grid_search = GridSearchCV(
    estimator  = rf_for_tuning,
    param_grid = param_grid,
    cv         = 5,           # 5-fold cross-validation
    n_jobs     = -1,          # use all available CPU cores
    verbose    = 1,           # print progress
    scoring    = 'f1'         # optimise for F1-Score (balances Precision and Recall)
)

# ── Run the grid search on the resampled training data ────────────────────────
print("Starting GridSearchCV — this may take several minutes...")
